     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $certificates = Certificate::where('status', null)->get();
        $sent = Certificate::where('status', 'sent')->get();
        return view('certificate.index',[
            'certificates' => $certificates,
            'sent' => $sent
        ]);
    }

    public function start(){
        set_time_limit(600);
        $certificates = Certificate::where('status', null)->get();

        foreach ($certificates as $certificate) {
            Mail::to($certificate->email)->queue(new CertificateMail($certificate));
            $certificate->update([
                'status' => 'sent'
            ]);

        }
        return back();
    }


